import datetime


class Item(object):
    def __init__(self, word, index):
        self._word = word
        self.len = len(word)
        self._char_id_list = list(set([ord(c) - ord('a') for c in word]))
        _tmp_char_bitmap = [False] * 26
        for c in self._char_id_list:
            _tmp_char_bitmap[c] = True
        self._char_bitmap = tuple(_tmp_char_bitmap)
        # print self._char_list, self._char_bitmap

    def does_share_common_letter(self, other_item):
        for c in other_item._char_id_list:
            if self._char_bitmap[c]:
                return True
        return False


class Solution(object):
    def maxProduct(self, words):
        """
        :type words: List[str]
        :rtype: int
        """
        t1 = datetime.datetime.now()

        words.sort(key=lambda w: len(w), reverse=True)

        t2 = datetime.datetime.now()
        print (t2-t1).total_seconds()
        t1 = t2

        items = [Item(word, i) for i, word in enumerate(words)]
        heap = []  # (Item, next_index, value)
        for i, item in zip(range(len(items) - 1), items):
            heap.append((item, i + 1, item.len * items[i + 1].len))
        heap_size = len(heap)

        t2 = datetime.datetime.now()
        print (t2-t1).total_seconds()
        t1 = t2

        while heap_size > 0:
            item, next_index, value = heap[0]
            # print item._word, items[next_index]._word, value
            if not item.does_share_common_letter(items[next_index]):
                t2 = datetime.datetime.now()
                print (t2-t1).total_seconds()
                return value

            next_index += 1
            if next_index < len(items):
                heap[0] = (item, next_index, item.len * items[next_index].len)
            else:
                heap_size -= 1
                heap[0] = heap[heap_size]

            parent_index = 0
            while True:
                max_value_index = parent_index
                left_child_index = parent_index * 2 + 1
                right_child_index = parent_index * 2 + 2
                if left_child_index < heap_size:
                    max_value_index = self._get_max_index(heap, max_value_index, left_child_index)
                if right_child_index < heap_size:
                    max_value_index = self._get_max_index(heap, max_value_index, right_child_index)
                if max_value_index == parent_index:
                    break
                else:
                    heap[parent_index], heap[max_value_index] = heap[max_value_index], heap[parent_index]
                    parent_index = max_value_index
            # print parent_index
        return 0

    def _get_max_index(self, heap, index1, index2):
        return index1 if heap[index1][2] >= heap[index2][2] else index2


